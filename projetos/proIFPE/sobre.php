	<h1>Projeto de proposta para o ProIFPE</h1>
	<br>
	<h2>Informações sobre o projeto</h2>
	<p>Projeto de alunos do Instituto Federal de Ciência e Tecnologia de Pernambuco - Campus Igarassu</p>
	<p>Cadeira - Projeto e prática I</p>
	<p>Docente - Alexandre Strapação Guedes Vianna</p>
	<p>Professor Idealizador - Francisco Chaves Pinto</p>
	<br>
	<p>Este projeto está aos encargos de nosso grupo (Heal-Lab) e tem como responsabilidade desenvolver um sistema voltado para o proIFPE, contribuindo para melhorias para esta modalidade de ensino e tentará trazer alguns conceitos de utilização como:</p>
	<br>
	<ul>
		<li>Matrículas de alunos por disciplinas ofertadas</li>
		<li>Controle de presenças dos alunos</li>
		<li>Registro de aulas ministradas</li>
		<li>Armarzenamento de avaliações e notas</li>
	</ul>
	<br>
	<p>Podendo obter mais funcionalidades durante o processo de desenvolvimento.</p>
	<p>O projeto tem como objetivo principal, criar um site que compute as faltas e notas de todos os alunos do PROIFPE.</p>
	<p>A nossa expectativa é que esse site funcione em nossa instituição.</p>
